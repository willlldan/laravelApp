     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'nama' => 'required',
            'nrp' => 'required|size:9',
            'email' => 'required',
            'jurusan' => 'required'
        ]);
        Student::create($request->all());

        return redirect('/students')->with('status', 'Data Berhasil ditambah');
    }

